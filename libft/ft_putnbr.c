#include "libft.h"

void	ft_putnbr(int n)
{
	long nbl;

	nbl = n;
	if (nbl >= -2147483648 && nbl <= 2147483647)
		if (n < 0)
		{
			ft_putchar('-');
			nbl *= -1;
		}
	if (nbl > 9)
		ft_putnbr(nbl / 10);
	ft_putchar(nbl % 10 + '0');
}
